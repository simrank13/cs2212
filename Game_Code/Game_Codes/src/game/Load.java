    /** 
     * open the selected game
     * @param evt 
     */

    private void loadButtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadButtActionPerformed
        /** Selected a slot and game isn't empty */
        if (level != 0 && game.getIncorrect() > 0) {
            /** Player already completed the game. Start a new game in the same level. */
            if (game.getScores() > 100) {
                GamePlay newGame = new GamePlay(game.getLevel());
                Play play = new Play(player, newGame);
                play.setVisible(true);
                dispose();                    
            }
            else {
                Play play = new Play(player,game);
                play.setVisible(true);
                dispose();              
            }
        }
        if (game.getIncorrect() <= 0 && level != 0) {
                GamePlay newGame = new GamePlay(game.getLevel());
                Play play = new Play(player, newGame);
                play.setVisible(true);
                dispose();                    
        }
    }//GEN-LAST:event_loadButtActionPerformed


