    /**
     * Slot display
     */
    private void display(){
        easy.readCSVAndStore(player.getId());
        easy.loadGame(0);
        norm.readCSVAndStore(player.getId());
        norm.loadGame(1);
        hard.readCSVAndStore(player.getId());
        hard.loadGame(2);
        
        if (easy.getCurrentQuestion() != null){
            easySlot.setText("Level #" + (easy.getLevel()+1));
            easyPT.setText(easy.getScores() + " PT");
            easyLive.setText(easy.getIncorrect()+" ");        
        } else {
            easySlot.setBackground(new java.awt.Color(102, 102, 102));
            easySlot.setText("No Game Saved...");
        }
                
        if (norm.getCurrentQuestion() != null){
            normSlot.setText("Level #" + (norm.getLevel()+1));
            normPT.setText(norm.getScores() + " PT");
            normLive.setText(norm.getIncorrect()+" ");
        }else {
            normSlot.setBackground(new java.awt.Color(102, 102, 102));
            normSlot.setText("No Game Saved...");
        }
      
        if (hard.getCurrentQuestion() != null){
            hardSlot.setText("Level #" + (hard.getLevel()+1));
            hardPT.setText(hard.getScores() + " PT");
            hardLive.setText(hard.getIncorrect()+" ");
        } else {
            hardSlot.setText("No Game Saved...");
            hardSlot.setBackground(new java.awt.Color(102, 102, 102));
        }        
    }

