/**
 * Updates the Medium High Score table with the data from mediumArray.
 * It creates a new DefaultTableModel, adds columns, and populates the table with the data.
 */
        private void updateMediumHSTable() {
    // Create a new DefaultTableModel
    DefaultTableModel model = new DefaultTableModel();
    
    // Add columns to the model
    model.addColumn("User");
    model.addColumn("Top Highscores");
    
    // Add data from easyArray to the model
    for (int i = 0; i < mediumArray[0].length; i++) {
        model.addRow(new Object[]{mediumArray[0][i], mediumArray[1][i]});
    }
    
    // Set the new model to the jEasyModeHighScoreTable
    jMedModeHighScoreTable.setModel(model);
}

