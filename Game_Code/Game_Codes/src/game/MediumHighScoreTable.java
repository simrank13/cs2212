    private void getMediumData() {
        Map <String, Integer> easyScoresDict = new HashMap<>();
        LinkedHashMap <String, Integer> sortedEasyScores = new LinkedHashMap();
        ArrayList <Integer> placeHolderList = new ArrayList<>();
        for(int i = 0; i < Data.dataList.size(); i++) {
            easyScoresDict.put(Data.dataList.get(i).getId(), Data.dataList.get(i).getHighscore()[1]); 
        }
        for(Map.Entry<String, Integer> entry : easyScoresDict.entrySet()){
            placeHolderList.add(entry.getValue());
        }
        Collections.sort(placeHolderList, Collections.reverseOrder());
        for(int num : placeHolderList){
            for(Map.Entry<String, Integer> entry : easyScoresDict.entrySet()){
                if(entry.getValue().equals(num)){
                    sortedEasyScores.put(entry.getKey(), num);
                }
            }
        }
        mediumArray = new String [2][sortedEasyScores.size()];
        Set<String> keys = sortedEasyScores.keySet();
        int j = 0;
        for (String key : keys){
            mediumArray[0][j] = key;
            mediumArray[1][j] = String.valueOf(sortedEasyScores.get(key));
            j++;
        }
    }
    
